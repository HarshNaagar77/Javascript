// Notes 
// -> Primitive Datatype
//.  1. String,number,bigint etc 
//.  2. call by value (returns copy of called value so original value can not be changed)

// -> Non-primitive Datatypes
//.   1. Array,list,Objects etc 
//.   2. call by reference (returns original value and it can be change)

// -> Stack 
//.   All primitive types get stored in stacks in memory

// -> Heap
//.   All non-primitive types get stored in Heap in memory

// End of notes


   